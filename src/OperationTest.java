
public class OperationTest {

	public static void main(String[] args)
	{

		int number; //Declaration statement
		number = 12; //Assignment statement -> intialization 
		number = number + 5;
		System.out.println(number);
		number/=2;
		System.out.println(number);
		number-=6;
		System.out.println(number);
		number*=4;
		System.out.println(number);
		number%=2;
		System.out.println(number);
	}

}
